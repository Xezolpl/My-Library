package pl.xezolpl.mylibrary.models;

public class Category {
    private int nameR, imgR;

    public Category(int nameR, int imgR) {
        this.nameR = nameR;
        this.imgR = imgR;
    }

    public int getNameR() {
        return nameR;
    }

    public int getImgR() {
        return imgR;
    }

}
